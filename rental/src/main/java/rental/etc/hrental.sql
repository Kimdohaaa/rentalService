SELECT s.sname AS store_name, YEAR(r.rdate) AS year, SUM(r.rprice) AS anunnal_revenue 
FROM rental r 
JOIN store s ON r.sno = s.sno 
where s.sno= 1
GROUP BY s.sname, YEAR(r.rdate) 
ORDER BY s.sname ASC, year ASC; 

SELECT s.sname AS store_name, MONTH(r.rdate) AS month, SUM(r.rprice) AS month_revenue 
FROM rental r 
JOIN store s ON r.sno = s.sno 
WHERE s.sno = 1 AND YEAR(r.rdate) = YEAR(CURDATE()) 
GROUP BY s.sname, MONTH(r.rdate) 
ORDER BY s.sname ASC, month ASC;

SELECT s.sname AS store_name, DAY(r.rdate) AS date, SUM(r.rprice) AS day_revenue 
FROM rental r 
JOIN store s ON r.sno = s.sno 
WHERE s.sno = 1 AND YEAR(r.rdate) = YEAR(CURDATE()) 
AND MONTH(r.rdate) = MONTH(CURDATE()) 
GROUP BY s.sname, DAY(r.rdate) 
ORDER BY s.sname ASC, date ASC;




